def smart_sum(a, b, func, *args):
    sum = 0
    for x, y in zip(a, b):
        for z, r in zip(x, y):
            if len(args) == 0:  # abs function
                sum += func(z - r)
            elif args[0] == "Che":  # Chebishov
                if func(z - r) == 1:
                    return 1
            else:  # pow function
                sum += func(z - r, args[0])
    return sum


